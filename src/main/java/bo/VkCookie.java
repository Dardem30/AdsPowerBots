package bo;

public class VkCookie {
    private int id;
    private String name;
    private String value;
    private int vkAccId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getVkAccId() {
        return vkAccId;
    }

    public void setVkAccId(int vkAccId) {
        this.vkAccId = vkAccId;
    }
}
